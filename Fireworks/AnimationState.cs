namespace FireworkExperiment.Fireworks;

/// <summary>
/// Defines the animation state.
/// </summary>
public enum AnimationState
{
    /// <summary>
    ///  The animation is not running.
    /// </summary>
    Stopped,
    /// <summary>
    /// The animation is running.
    /// </summary>
    Running,
    /// <summary>
    /// The animation is stopped.
    /// </summary>
    Paused
}
